
public class Data
{
  int an;
  int luna;
  int zi;
  int ora;
  int minut;
  
  Data( int an, int luna, int zi, int ora, int minut )
  {
    this.an = an;
    this.luna = luna;
    this.zi = zi;
    this.ora = ora;
    this.minut = minut;
  }

  public int getAn()
  {
    return an;
  }

  public void setAn(int an)
  {
    this.an = an;
  }

  public int getLuna()
  {
    return luna;
  }

  public void setLuna(int luna)
  {
    this.luna = luna;
  }

  public int getZi()
  {
    return zi;
  }

  public void setZi(int zi)
  {
    this.zi = zi;
  }

  public int getOra()
  {
    return ora;
  }

  public void setOra(int ora)
  {
    this.ora = ora;
  }

  public int getMinut()
  {
    return minut;
  }

  public void setMinut(int minut)
  {
    this.minut = minut;
  }
  
  
}
